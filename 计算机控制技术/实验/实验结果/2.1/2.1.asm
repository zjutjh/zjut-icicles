CODE SEGMENT
ASSUME CS:CODE
START: MOV AL,00H
MOV BL,00H
AGAIN: MOV DX,0640H
OUT DX,AL
XCHG AL,BL
MOV DX,0641H
OUT DX,AL
XCHG AL,BL
CALL DELAY
INC AL ;变量1加1 锯齿波
JNZ AGAIN ;变量1不等于0跳转
NOT BL ;变量2取反
JMP AGAIN
DELAY: PUSH CX ;延时子程序
MOV CX,00FFFH
DEL1: PUSH AX
POP AX
LOOP DEL1
POP CX
RET
CODE ENDS
END START